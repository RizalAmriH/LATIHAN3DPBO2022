class CPU :


    def __init__(self, name= 0, price = 0):
        self.name = name
        self.price = price
        
    def set_CPUname(self, name):
        self.name = name    

    def set_CPUprice(self, price):
        self.price = price

    
    def get_CPUname(self):
        return self.name

    def get_CPUprice(self):
        return self.price

 
       
